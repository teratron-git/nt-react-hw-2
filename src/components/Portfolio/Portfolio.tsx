interface IProps {}

interface IState {
  filters: string[]
  selected: string
}

export interface IData {
  img: string
  category: string
}

export class Portfolio extends Component<IProps, IState> {
  constructor(props: IProps) {
    super(props)

    this.state = {
      filters: ["All", "Websites", "Flayers", "Business Cards"],
      selected: "All",
    }

    this.onSelectFilter = this.onSelectFilter.bind(this)
  }

  onSelectFilter(filter: string) {
    this.setState({ selected: filter })
  }

  filterProjectsByCategory(projects: IData[]) {
    if (this.state.selected === "All") {
      return projects
    }

    return projects.filter((project) => project.category === this.state.selected)
  }

  render() {
    return (
      <div className={st.portfolio}>
        <div className={st.container}>
          <Toolbar filters={this.state.filters} selected={this.state.selected} onSelectFilter={this.onSelectFilter} />
          <ProjectList projects={this.filterProjectsByCategory(data)} />
        </div>
      </div>
    )
  }
}

export default Portfolio
